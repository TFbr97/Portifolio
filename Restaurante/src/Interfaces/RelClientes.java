    private void mostratabela(){
        
        tbClientes.getColumnModel().getColumn(0).setPreferredWidth(10);
        tbClientes.getColumnModel().getColumn(1).setPreferredWidth(100);
        tbClientes.getColumnModel().getColumn(2).setPreferredWidth(80);
        tbClientes.getColumnModel().getColumn(3).setPreferredWidth(100);
        
        DefaultTableModel modelo = (DefaultTableModel)tbClientes.getModel();
            modelo.setNumRows(0);
            
            ClienteDao dao = new ClienteDao();
            
            List<Cliente> clientes = dao.BuscaClientes();
            
            for(Cliente cliente : clientes){
                
                modelo.addRow(new Object[]{
                
                    cliente.getIdcliente(),
                    cliente.getNome(),
                    cliente.getTelefone(),
                    cliente.getEmail()                             
                
                });
                
                
            }
        
    }    
    
